"""
[DOMAIN: GENERIC]
Playwright Writer — generates flat retest scripts from enriched defect data.

Strategy: Option C — deterministic template + EnrichedDefect data.
No AI involved in script generation. Output is a valid Python file
with clear TODO markers where manual verification is needed.

Why flat script (not POM):
- Readable by any tester, not just automation engineers
- Easy to edit when selectors change
- TODO markers make gaps explicit — no silent failures
- Good enough for v1; POM/framework in qa-automation-framework project

Salesforce / Shadow DOM note:
- SF LWC components use Shadow DOM — standard CSS selectors won't pierce it
- Playwright pierce syntax: page.locator("pierce/#selector")
- Aura component IDs (e.g. #\\31 2497\\:0) are dynamic — change between sessions/releases
- AI-extracted selectors from comments are hints, not guaranteed stable
- Always verify selectors manually before adding to CI
"""

